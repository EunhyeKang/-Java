package org.opentutorials.javatutorials.eclipse;

public class SwitchDemo {
	public static void main(String[] agrs) {
		
		switch(4) {
		case 1:
			System.out.println("one");
			break;
		case 2:
			System.out.println("two");
			break;
		case 3:
			System.out.println("three");
			break;
		default:
			System.out.println("default");
		}
	}
}
